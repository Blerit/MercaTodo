     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);        
        $user->fill($request->all());        
        $user->save();
        return redirect()->route('users.index');
    }

    public function statusUpdate(Request $request, $id)
    {
